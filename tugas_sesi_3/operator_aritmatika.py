#Operator Aritmatika
numA = int(input("Masukkan angka pertama: "))
numB = int(input("Masukkan angka kedua: "))

penjumlahan = numA + numB
pengurangan = numA - numB
perkalian = numA * numB
pembagian = numA / numB

print("\n")
print("Hasil Penjumlahan = ", penjumlahan)
print("Hasil Pengurangan = ", pengurangan)
print("Hasil Perkalian = ", perkalian)
print("Hasil Pembagian = ", pembagian)
